class TestGetCapabilities:
    def test_returns_capability_descriptor(self, worker: LocalWorker) -> None:
        caps = worker.get_capabilities()
        assert isinstance(caps, CapabilityDescriptor)

    def test_capability_fields_present(self, worker: LocalWorker) -> None:
        """All CapabilityDescriptor fields per DESIGN.md §Schema."""
        caps = worker.get_capabilities()
        expected_fields = {
            "cpu_cores", "memory_total_mb", "memory_free_mb", "disk_free_gb",
            "ffmpeg_encoders", "gpu_available", "npu_available", "platform",
        }
        assert expected_fields.issubset(caps.keys())

    def test_platform_matches_system(self, worker: LocalWorker) -> None:
        import platform as _p
        caps = worker.get_capabilities()
        assert caps["platform"] == _p.system().lower()

    def test_ffmpeg_encoders_is_list(self, worker: LocalWorker) -> None:
        caps = worker.get_capabilities()
        assert isinstance(caps["ffmpeg_encoders"], list)
        # Whether ffmpeg is installed varies; if installed, may have entries
        if caps["ffmpeg_encoders"]:
            assert all(isinstance(e, str) for e in caps["ffmpeg_encoders"])

    def test_npu_always_false_in_v1(self, worker: LocalWorker) -> None:
        """DESIGN.md: v1 worker get_capabilities() leaves NPU as false (no detection yet)."""
        caps = worker.get_capabilities()
        assert caps["npu_available"] is False


