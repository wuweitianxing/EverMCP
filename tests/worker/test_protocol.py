# list_tools — returns ToolDescriptor shape (DESIGN.md §Worker Protocol)
# ---------------------------------------------------------------------------

class TestListTools:
    def test_returns_list_of_descriptors(self, worker: LocalWorker) -> None:
        descriptors = worker.list_tools()
        assert isinstance(descriptors, list)
        assert len(descriptors) == 4

    def test_descriptor_shape(self, worker: LocalWorker) -> None:
        descs = {d["name"]: d for d in worker.list_tools()}
        d = descs["ok.echo"]
        # ToolDescriptor has: name, description, input_schema, category
        assert d["name"] == "ok.echo"
        assert d["description"] == "Echo the input back"
        assert d["category"] == "ok"
        assert d["input_schema"]["type"] == "object"
        assert "msg" in d["input_schema"]["properties"]

    def test_includes_all_registered_tools(self, worker: LocalWorker) -> None:
        names = {d["name"] for d in worker.list_tools()}
        assert names == {"ok.echo", "fail.always_fail", "bad.bad_return", "sec.violate"}


