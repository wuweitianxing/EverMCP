class TestCallToolNotFound:
    def test_unknown_tool_returns_error(self, worker: LocalWorker) -> None:
        result = worker.call_tool("nonexistent.tool", {}, call_id="x")
        assert result["success"] is False
        assert result["result"] is None
        err = result["error"]
        assert err["code"] == TOOL_NOT_FOUND
        assert err["code"] == -32001
        assert "not found" in err["message"].lower()

    def test_error_envelope_shape(self, worker: LocalWorker) -> None:
        """Error data should include tool name and args for debugging."""
        result = worker.call_tool("missing.tool", {"foo": "bar"})
        err = result["error"]
        assert err["data"]["tool"] == "missing.tool"
        assert err["data"]["args"] == {"foo": "bar"}


