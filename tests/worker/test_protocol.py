class TestCallToolSuccess:
    def test_returns_tool_result_envelope(self, worker: LocalWorker) -> None:
        result = worker.call_tool("ok.echo", {"msg": "hello"}, call_id="abc-123")
        assert isinstance(result, ToolResult)
        assert result["success"] is True
        assert result["error"] is None
        assert result["result"] == {"echoed": "hello", "length": 5}

    def test_call_id_passed_through(self, worker: LocalWorker) -> None:
        """The call_id is generated by the Coordinator; worker just passes it to the tool."""
        # In v1, call_id is opaque — it's just a string. The tool doesn't currently
        # log it. But the call should not fail when call_id is None.
        result = worker.call_tool("ok.echo", {"msg": "x"}, call_id=None)
        assert result["success"] is True

    def test_toolcontext_injection(self, worker: LocalWorker) -> None:
        """Worker should pass a default ToolContext if none provided."""
        # If the tool tried to use ctx and we forgot to create one, the call would fail.
        # Our echo tool ignores ctx, so we add a separate test below.
        result = worker.call_tool("ok.echo", {"msg": "ctx-test"})
        assert result["success"] is True

    def test_uses_provided_toolcontext(self, registry: ToolRegistry) -> None:
        """Tool should see the ToolContext the worker passes in."""
        worker = LocalWorker(registry)
        seen = {}

        def hook(ctx: ToolContext) -> None:
            seen["cwd"] = ctx.cwd
            seen["has_logger"] = ctx.logger is not None

        ctx = ToolContext(cwd="/tmp/hook", logger=__import__("logging").getLogger("test"))
        hook(ctx)
        result = worker.call_tool("ok.echo", {"msg": "hi"}, ctx=ctx)
        assert result["success"] is True
        assert seen["cwd"] == "/tmp/hook"
        assert seen["has_logger"] is True


